package week_1;

public class DataTypesDemo {
    public static void main(String[] args) {
        // there are two primary classes of data
        // primitive data types and object data type
        // Java comes with 8 primitive data types

        // this is binary number 14
        // 0 0 1 1 1 0

        // boolean can be true or false - ONLY
        boolean awake = true;

        // char - a single character
        char charVal = 'B';

        // integer data types
        byte byteVal = 127; // 8 bits
        short shortVal = 12234; // 16 bits
        int intVal = 1345135413; // 32 bits
        long longVal = 7456436153111L; // 64 bits

        // floating-point numbers (decimals)
        float floatVal = 46534.3543F; // 32 bits
        double doubleVal = 564753745363.435352; // 64 bits
    }
}
